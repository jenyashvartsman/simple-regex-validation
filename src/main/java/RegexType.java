public enum RegexType {
	USERNAME("^[A-z0-9_-]{3,15}$"),
	PASSWORD("((?=.*\\d)(?=.*[a-z])(?=.*[A-Z])(?=.*[@#$%]).{6,20})"),
	HEXADECIMAL("^#([A-Fa-f0-9]{6}|[A-Fa-f0-9]{3})$"),
	EMAIL("^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$"),
	IMAGE_FILE_EXTENSION("([^\\s]+(\\.(?i)(jpg|jpeg|png|gif|bmp))$)"),
	IP_ADDRESS("^([01]?\\d\\d?|2[0-4]\\d|25[0-5])\\.([01]?\\d\\d?|2[0-4]\\d|25[0-5])\\.([01]?\\d\\d?|2[0-4]\\d|25[0-5])\\.([01]?\\d\\d?|2[0-4]\\d|25[0-5])$"),
	TIME_12_HOUR_FORMAT("(1[012]|[1-9]):[0-5][0-9](\\s)?(?i)(am|pm)"),
	TIME_24_HOUR_FORMAT("([01]?[0-9]|2[0-3]):[0-5][0-9]"),
	DATE_FORMAT_DDMMYYYY("^(?:(?:31(\\/|-|\\.)(?:0?[13578]|1[02]))\\1|(?:(?:29|30)(\\/|-|\\.)(?:0?[1,3-9]|1[0-2])\\2))(?:(?:1[6-9]|[2-9]\\d)?\\d{2})$|^(?:29(\\/|-|\\.)0?2\\3(?:(?:(?:1[6-9]|[2-9]\\d)?(?:0[48]|[2468][048]|[13579][26])|(?:(?:16|[2468][048]|[3579][26])00))))$|^(?:0?[1-9]|1\\d|2[0-8])(\\/|-|\\.)(?:(?:0?[1-9])|(?:1[0-2]))\\4(?:(?:1[6-9]|[2-9]\\d)?\\d{2})$"),
	HTML_TAG("<(\"[^\"]*\"|'[^']*'|[^'\">])*>");
	
	private String regex;
	
	RegexType(String regex) {
		this.regex = regex;
	}
	
	public String getRegex() {
		return regex;
	}
}
